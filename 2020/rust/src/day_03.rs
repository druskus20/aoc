#[aoc_generator(day3)]
pub fn input_gen(input: &str) -> Vec<String> {
    input.lines().map(|l| l.to_string()).collect()
}

pub fn count_slope(data: &Vec<String>, rule: &(usize, usize)) -> usize {
    let line_total = data.get(0).unwrap().len();
    let mut index = 0;

    data.iter()
        .skip(rule.1)
        .enumerate()
        .filter(|(idx, _)| idx % rule.1 == 0)
        .map(|(_, x)| x)
        .filter(|l| {
            index = (index + rule.0) % line_total;
            l.chars().nth(index).unwrap() == '#'
        })
        .count()
}

#[aoc(day3, part1)]
pub fn solve_part1(data: &Vec<String>) -> usize {
    count_slope(data, &(3, 1))
}

#[aoc(day3, part2)]
pub fn solve_part2(data: &Vec<String>) -> usize {
    let slopes: [(usize, usize); 5] = [(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)];
    let mut total_mult = 1;

    slopes.iter().for_each(|s| {
        total_mult *= count_slope(data, s);
    });

    total_mult
}
